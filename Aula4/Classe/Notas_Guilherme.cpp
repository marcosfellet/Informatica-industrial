/* "Struct turbinada"
Paradigma a orientacao de objetos: defini funcoes distintas para partes do codigo, as quais passem a ser independentes. Exemplo: classe sensor, classe controlador, etc
classe sensor capta os sinais, classe controlador pega a saida da classe sensor e trabalha em cima disso, etc

Vantagem: aumento da escalabilidade das variaveis, ou seja, variaveis inegradas entre si

Classes = modelo
Funcao da classe: modelo para criar objetos

Objetos, separacao: Atributos => dados
           Metodos => funcionalidades
Analogia:
int var           
int (classe) = modelo
var (objeto) = variavel

Exemplo:
Casse: carro
  Objeto: Honda Civic, Fusca, HB20, etc
      => Atributo (dado): corro, quantidade de portas, potencia, etc
      => Metodos (funcionalidade/acao): acelerar, frear, buzinar, etc

Classe comeca com letra maiuscula
*/
